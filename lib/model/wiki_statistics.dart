class WikiStatistics {
  const WikiStatistics({
    this.pageCount = 0,
    this.articleCount = 0,
    this.editCount = 0,
    this.imageCount = 0,
    this.userCount = 0,
    this.activeUserCount = 0,
  });

  final int pageCount;
  final int articleCount;
  final int editCount;
  final int imageCount;
  final int userCount;
  final int activeUserCount;
}
